#include <iostream>

int main() {
    int numero1, numero2, multiplicacion;


    std::cout << "ingrese el primer numero: ";
    std::cin >> numero1;

    std::cout << "ingrese el segundo numero: ";
    std::cin >> numero2;


    multiplicacion = numero1 * numero2;


    std::cout << numero1 << " * " << numero2 << " = " << multiplicacion << std::endl;

    return 0;
}



